def tenPow(n):             # returns 10 to the power of n
  ans = 1
  for i in range(n):
    ans *= 10
  return ans

def mod(a, b):             # returns the remainder from dividing a by b
  return a - ((a//b) * b)

def kthDigit(x, k):        # returns the kth digit of integer x 
  return mod(x // tenPow(k-1), 10)

